#include<iostream>
using namespace std;

int main() {

    int a; //declaration
    a=12; //initialisation

    cout<<"size of int "<<sizeof(a)<<endl;


    float b;
    cout<<"size of int "<<sizeof(b)<<endl;

    char c;
    cout<<"size of int "<<sizeof(c)<<endl;

    bool d;
    cout<<"size of int "<<sizeof(d)<<endl;

    //type modifiers can be used for a number to make it
    //signed or unsigned or long or short based on the needs.

    short int si;
    long int li;

    cout<<"size of shortint "<<sizeof(si)<<endl;
    cout<<"size of longint "<<sizeof(li)<<endl;



}