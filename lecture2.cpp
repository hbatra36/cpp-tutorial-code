#include<iostream> //preprocessor directive includes c++ files.
using namespace std;//this can be specified here as a good practice instead of using everytime in front of a cout.
//iostream is the header file for taking input and printing output.

int main() { //this tells the compiler from where to start the code.

        cout<<"Hello World!\n"; //cout is used to display output in quotation marks.
        // \n is general newline char for linebreak.
        // ; indicates the end of a line.
        return 0; // this is the exit status of a function.
        
} // braces indicate the start and end of a function.

